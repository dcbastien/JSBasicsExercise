// STRINGS (Content inside of single or double quotes)

// Only the content inside of the single or double quotes will display in the terminal/console
console.log("Matt Riley");
console.log('Matt Riley');

// Examples of strings with single or double quotes inside the string
console.log('Captain America said, "Avengers Assemble!"');
console.log("You can't handle the truth!");
// console.log('This doesn't work');
// The backslash "\" (AKA Escape character) allows for special characters to be used in a string (AKA Turns special characters into string characters.)
// NOTE: The backslash will not appear in the terminal/console
console.log('This\'ll work');

// Concatenation (Adding strings together using the "+" plus sign)
console.log("Commonwealth" + "Coders");
console.log("Commonwealth " + "Coders");
console.log("Commonwealth" + " " + "Coders");

// Length Property (Returns the length/number of characters and spaces in the string)
console.log("BCSDA".length);
console.log("Commonwealth Coders".length);
console.log("Hi".length + "Bye".length);
// console.log(2 + 3);

// Index
// Each character or space in a string has a set index
// Index starts at ZERO
console.log("Hello"[1]);
console.log("Hello"[0]);
console.log("Hello"[4]);